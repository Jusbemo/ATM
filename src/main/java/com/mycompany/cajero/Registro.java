    private boolean isInvalidName(String name) {
        return name.isEmpty() || name.equals("Ingresa tu nombre");
    }

    private boolean isInvalidLastName(String lastName) {
        return lastName.isEmpty() || lastName.equals("Ingresa tu apellido");
    }

    private boolean isInvalidUsername(String username) {
        if (username.isEmpty() || username.equals("Ingresa tu nombre de usuario")) {
            return true;
        }
        Conexion connectionObject = new Conexion();

        String sql = "SELECT * FROM usuario WHERE username = ?";

        try {
            PreparedStatement ps = connectionObject.establishConnection().prepareStatement(sql);
            ps.setString(1, username);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                return true;
            }
        } catch (SQLException ex) {

            return true;
        }

        return false;
    }

    private boolean isInvalidPin(String pin) {
        if (pin.isEmpty() || pin.equals("******") || pin.length() != 4) {
            return true;
        }

        try {
            Integer.parseInt(pin);
        } catch (NumberFormatException e) {
            return true;
        }

        return false;
    }

    private boolean isInvalidAmountAccounts(String amountAccountsString) {
        if (amountAccountsString.isEmpty() || amountAccountsString.equals("Ingresa el nÃºmero de cuentas a crear (mÃ¡ximo 3)")) {
            return true;
        }

        try {
            int amountAccounts = Integer.parseInt(amountAccountsString);
            if (amountAccounts > 3) {
                return true;
            }
        } catch (NumberFormatException e) {
            return true;
        }

        return false;
    }


    private void createLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_createLabelMouseClicked
        String name = nameText.getText();
        String lastname = lastnameText.getText();
        String username = usernameText.getText();
        String pin = String.valueOf(pinText.getPassword());
        String amountAccountsString = cuentasText.getText();

        if (isInvalidName(name)) {
            JOptionPane.showMessageDialog(null, "Ingresa tu nombre adecuadamente");
        } else if (isInvalidLastName(lastname)) {
            JOptionPane.showMessageDialog(null, "Ingresa tu apellido adecuadamente");
        } else if (isInvalidUsername(username)) {
            JOptionPane.showMessageDialog(null, "Ingresa tu nombre de usuario adecuadamente. El nombre de usuario ya existe o aÃºn no se ha ingresado un valor");
        } else if (isInvalidPin(pin)) {
            JOptionPane.showMessageDialog(null, "Ingresa el PIN adecuadamente (MÃ¡ximo 4 dÃ­gitos numÃ©ricos)");
        } else if (isInvalidAmountAccounts(amountAccountsString)) {
            JOptionPane.showMessageDialog(null, "Ingresa un nÃºmero vÃ¡lido de cuentas");
        } else {
            Usuario usuario = new Usuario("", "", "", 0, 0);

            usuario.createUser(nameText, lastnameText, usernameText, pinText, cuentasText);
            BigDecimal balance = new BigDecimal(0);

            Cuenta cuenta = new Cuenta(0, balance);

            for (int i = 0; i < Integer.parseInt(cuentasText.getText()); i++) {
                String accountNumberString = cuenta.createUniqueAccountNumber();

                long accountNumber = Long.parseLong(accountNumberString);
                cuenta.createAccount(accountNumber, balance, usernameText.getText());
            }

            JOptionPane.showMessageDialog(null, "SE CREO EL USUARIO Y LAS CUENTAS");
        }


    }//GEN-LAST:event_createLabelMouseClicked

